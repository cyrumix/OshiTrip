-- ============================================================================
-- 0009_todo_item_type.sql — Todo/持ち物の種別追加（型安全な分類）
--
-- 目的:
--   現場詳細の「Todo」を拡張し、同じ仕組みで「持ち物」も管理できるようにする。
--   任意文字列のタグではなく、check制約付きの安定した値（'todo'/'belonging'）
--   で型安全な分類を持たせる。
--
-- 既存データ移行:
--   既存の todos 行はすべて種別列を持たないため、デフォルト値 'todo' を
--   付与し、既存データを失わずそのまま「Todo」として扱う。
-- ============================================================================

alter table public.todos
  add column if not exists type text not null default 'todo'
    check (type in ('todo', 'belonging'));
